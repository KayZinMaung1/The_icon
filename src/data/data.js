  export function getRelatedBlogs() {
    var IDs = [];
    var blogs = [];
    while(IDs.length < 3){
      var ID = Math.floor(Math.random() * BLOGS.length) + 1;
      if(IDs.indexOf(ID) === -1) IDs.push(ID);
    }
    IDs.map(ID => (
     blogs.push( BLOGS.find(BLOG => BLOG.id === ID ))
    ))
    return blogs;
  }

